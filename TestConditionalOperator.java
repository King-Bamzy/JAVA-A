public class TestConditionalOperator {
    /**
     * @param args command line arguments
     */
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 20;

        // Use of conditional operators
        System.out.println("value1 == value2: " + (value1 == value2)); // Equality
        System.out.println("value1 != value2: " + (value1 != value2)); // Inequality
        System.out.println("value1 > value2: " + (value1 > value2));   // Greater than
        System.out.println("value1 >= value2: " + (value1 >= value2)); // Greater than or equal to
        System.out.println("value1 < value2: " + (value1 < value2));   // Less than
        System.out.println("value1 <= value2: " + (value1 <= value2)); // Less than or equal to
    }
}